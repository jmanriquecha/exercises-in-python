## Metodos con cadenas

texto = 'Hola mundo COmo estas'
print(f'Cadena original: {texto}')

#Mayusculas
mayusculas = texto.upper()
print(f'Cadena en MAYUSCULAS: {mayusculas}')

## Minusculas
print(f'Cadena en minuculas {texto.lower()}')

## Cadena con espacios en blanco

cadena_espacios = ' Esto es una cadena con espacios en blanco al inicio y al final '
print(cadena_espacios)

## Quitar espacios en blanco al inicio y al final
print(cadena_espacios.strip()) ##Elimina espacios al inicio y al final de la cadena de texto